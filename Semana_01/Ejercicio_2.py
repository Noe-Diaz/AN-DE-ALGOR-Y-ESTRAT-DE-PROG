# 1. Operaciones aritméticas
a = 10
b = 3
print(f"Suma: {a + b}, Resta: {a - b}, Multiplicación: {a * b}")
print(f"División: {a / b}, Módulo: {a % b}")


print("\n")
# 2. Operadores de asignación
x = 5
x += 2  # Incremento
x -= 1  # Decremento
print(f"Valor final de x: {x}")


print("\n")
# 3. Operadores de comparación
print(f"¿a es igual a b? {a == b}")
print(f"¿a es diferente de b? {a != b}")


print("\n")
# 4. Operadores lógicos
cond1 = a > 5
cond2 = b < 5
print(f"Ambas condiciones son verdaderas: {cond1 and cond2}")
